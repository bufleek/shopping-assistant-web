export async function getProducts({
  platform,
  query,
  base_url,
}: {
  platform: Platform;
  query: string;
  base_url: string;
}): Promise<ProductList> {
  return new Promise((resolve, reject) => {
    const url = `${base_url}?exec=products&platform=${platform.key}&query=${query}`;
    fetch(url)
      .then((response) => {
        resolve(response.json());
      })
      .catch((error) => reject(error.message));
  });
}
